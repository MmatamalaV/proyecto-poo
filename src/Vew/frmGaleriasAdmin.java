    public void llenarTabla(){
        tabla.setColumnCount(0);
        tabla.addColumn("nombre");
        tabla.addColumn("ubicación");
        ArrayList<Galerias> lista=cg.mostrar();
        tabla.setNumRows(lista.size());
        for (int i = 0; i < lista.size(); i++) {
            tabla.setValueAt(lista.get(i).getNombreGaleria(), i, 0);
            tabla.setValueAt(lista.get(i).getUbucacionGaleria(), i, 1);
        }
        jTable1.setModel(tabla);
    }
