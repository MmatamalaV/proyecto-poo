    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       sw=1;
        if (fila>-1) {
           int res=JOptionPane.showConfirmDialog(this, "¿desea editar la galeria: "+String.valueOf(jTable1.getValueAt(fila, 0))+"?","advertencia",JOptionPane.YES_NO_OPTION);
           if(res==0){
               nombreAnterior=String.valueOf(jTable1.getValueAt(fila, 0));
                v1.setText(String.valueOf(jTable1.getValueAt(fila, 0)));
                v2.setText(String.valueOf(jTable1.getValueAt(fila, 1)));
                jTabbedPane1.setSelectedIndex(1);
                fila=-1;
           }
        }else{
           JOptionPane.showMessageDialog(this, "debe seleccionar una fila","advertencia",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

