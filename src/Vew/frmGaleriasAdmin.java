    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (fila>-1) {
           int res=JOptionPane.showConfirmDialog(this, "¿desea eliminar la galeria: "+String.valueOf(jTable1.getValueAt(fila, 0))+"?","advertencia",JOptionPane.YES_NO_OPTION);
           if(res==0){
                cg.eliminarGaleria(String.valueOf(jTable1.getValueAt(fila, 0)));
                llenarTabla();
                JOptionPane.showMessageDialog(this, "galaeria eliminada","el museo",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
                fila=-1;
           }
        }else{
           JOptionPane.showMessageDialog(this, "debe seleccionar una fila","advertencia",JOptionPane.OK_OPTION+JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


