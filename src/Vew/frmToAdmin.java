    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ControlAdmin ctl= new ControlAdmin();
        int secret=0;
        String pass= String.valueOf(v2.getPassword());
        if (v1.getText().equals("") || pass.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar datos","el museo",JOptionPane.OK_OPTION+JOptionPane.INFORMATION_MESSAGE);
            v1.setText("");
            v2.setText("");
            v2.grabFocus(); 
        }else{
            
            try{
                secret=Integer.parseInt(pass);
                
            }catch(NumberFormatException ex){
            }
            if (ctl.login(secret, v1.getText())) {
                Principal.jButton5.setVisible(false);
                Principal.jButton6.setVisible(false);
                Principal.jToolBar3.setVisible(true);
                Principal.usuario.setText(Principal.usuario.getText()+" â˜…");
                this.dispose();
            }
            else{
                mensaje.setText("password o mail incorrecta");
                v1.setText("");
                v2.setText("");
                v1.grabFocus();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


