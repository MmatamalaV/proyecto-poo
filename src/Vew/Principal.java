    public static void centrar(JInternalFrame frm){
        Dimension contenedor= jDesktopPane1.getSize();
        Dimension hijo= frm.getSize();
        frm.setLocation((contenedor.width-hijo.width)/2,(contenedor.height-hijo.height)/2);
        frm.show();
    }
    public boolean verificar(JInternalFrame objeto){
        boolean estado= true;
        for (int i = 0; i < jDesktopPane1.getComponentCount(); i++) {
            if(objeto.getClass().isInstance(jDesktopPane1.getComponent(i))){
                estado=false;
            }
        }
        return !objeto.isShowing() && estado;
    }
